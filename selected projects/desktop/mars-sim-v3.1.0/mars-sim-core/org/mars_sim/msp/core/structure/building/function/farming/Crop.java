				if (msolCache != msol && msol % CHECK_HEALTH_FREQUENCY == 0) {
					msolCache = msol;
					// Checks on crop health
					trackHealth();
				}

				// max possible harvest within this period of time
				double maxPeriodHarvest = maxHarvest * (time / growingTime);
